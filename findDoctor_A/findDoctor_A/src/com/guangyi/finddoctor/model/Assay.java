package com.guangyi.finddoctor.model;

import java.io.Serializable;

public class Assay implements Serializable{
	
	/**
	 * 
	 */
	/**索引字母*/
	public String index;
	private static final long serialVersionUID = 1L;
	private String name;    //标题
	private String introduce; //介绍
	private String normalValue; // 正常值
	private String sense;  //  临床
	private String notes; // 注意事项
	private String createtime; //创建时间
	private String otherassay; //相关疾病
	private int id;
	
	
	public static String NAME="name";    //标题
	public static String INTRODUCE="introduce"; //介绍
	public static String NORMALVALUE="normalValue"; // 正常值
	public static String SENSE="sense";  //  临床
	public static String NOTES="notes"; // 注意事项
	public static String CREATETIME="createtime"; //创建时间
	public static String OTHERASSAY="otherassay"; //相关疾病
	public static String ID="id";
	
	
	
	public String getIndex() {
		return index;
	}
	public void setIndex(String index) {
		this.index = index;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntroduce() {
		return introduce;
	}
	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}
	public String getNormalValue() {
		return normalValue;
	}
	public void setNormalValue(String normalValue) {
		this.normalValue = normalValue;
	}
	public String getSense() {
		return sense;
	}
	public void setSense(String sense) {
		this.sense = sense;
	}
	public String getNotes() {
		return notes;
	}
	public void setNotes(String notes) {
		this.notes = notes;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
	public String getOtherassay() {
		return otherassay;
	}
	public void setOtherassay(String otherassay) {
		this.otherassay = otherassay;
	}
	
	


}
